/**
 * Chaînes UI — clés identiques pour chaque locale.
 * La locale active est dérivée de `navigator.language` (voir pickBrowserLocale).
 */

export function pickBrowserLocale() {
  if (typeof navigator === 'undefined') return 'fr';
  const raw = (navigator.languages && navigator.languages[0]) || navigator.language || '';
  const lower = String(raw).toLowerCase();
  if (lower.startsWith('fr')) return 'fr';
  if (lower.startsWith('en')) return 'en';
  return 'en';
}

